<template>
  <div class="login">
    <div class="main_login">
      <div class="title">登 录</div>
      <el-row type="flex" justify="center">
        <!-- 登录表单 -->
        <el-form ref="loginForm" :rules="rules" :model="loginForm">
            <el-form-item prop="username">
                <el-input @keyup.enter.native="login('loginForm')" icon="el-icon-search" placeholder="请输入用户名" v-model="loginForm.username">
                  <i class="el-icon-user" slot="prefix"> </i>
                </el-input>
            </el-form-item>
            <el-form-item prop="password">
                <el-input @keyup.enter.native="login('loginForm')" placeholder="请输入密码" v-model="loginForm.password" show-password>
                  <i class="el-icon-lock" slot="prefix"> </i>
                </el-input>
            </el-form-item>
            <!-- 登录按钮 -->
            <el-form-item style="text-align: center;">
              <el-button type="primary" @click="login('loginForm')" style="text-align: center;width: 150px" :loading="loading">登  录</el-button>
            </el-form-item>
          </el-form>
      </el-row>
      <!-- 注册和忘记密码链接 -->
      <div class="link">
        <el-button type="text" @click="mylogin = false">注册新账号</el-button>
      </div>
    </div>
  </div>
</template>
<script>
export default {
  name: 'Login',
  props:['loginshow','dialogshow'],
  data() {
    return {
      // 注册和登录dialog
      mylogin: false,
      mydialog: false,
      loading:false,
      // 表单数据
      loginForm: {
        username: '',  //用户名
        password: '',  //密码
      },
      rules: {
        //表单验证（用户名验证规则）
        username: [
          {required: true,message: '账号不能为空', trigger: 'blur'},
          { max: 20, message: '账号长度最长20位', trigger: 'blur' }
        ],
        //表单验证（密码验证规则）
        password: [
          {required: true, message: '密码不能为空', trigger: 'blur'},
          { min: 6, message: '密码长度最少为6位', trigger: 'blur' }
        ],
      },
    }
  },
//页面初始化
  mounted() {
  },
//方法定义
  methods: {
    //登录方法
    login(formName) {
      // 表单验证通过，可进行操作
      this.$refs[formName].validate((valid) => {
        if (valid) {
          this.loading = true;
          this.$axios.post('/api/useract',{
            trantype:'login',  //操作类型
            username:this.loginForm.username,  //用户名
            password:this.$md5(this.loginForm.password),  //密码md5加密
          })
            .then((response)=>{
              let code=response.data.respcode;//返回状态码
              let msg=response.data.respmsg;//返回信息
              if (code === "000000") {  //登录成功，并提示
                this.$notify({
                  type: 'success',
                  message: '欢迎你,' + this.loginForm.username + '!',
                  duration: 1500,
                  offset: 50
                });
                sessionStorage.setItem("username",this.loginForm.username)   //将用户名存入session中
                this.loading = false;
                this.mydialog = false;
                this.$emit("state");  //将状态传到base页面
              }
              else {
                this.$message.error(msg);
                this.loading = false;
              }
            })
            .catch((error) => {
                this.$message.error('请求异常');
                this.loading = false;
            });
        } else {
          return false;  //表单验证错误返回false
        }
      })
    },
  },
  watch:{
      mylogin:{
        handler(val){
          console.log('loginshow更新：',val)
          this.$emit('update:loginshow',val);
        },
        deep:true,
      },
      loginshow:{
        handler(val){
          this.mylogin=this.loginshow;
        },
        deep:true,
        immediate:true,
      },
      mydialog:{
        handler(val){
          this.$emit('update:dialogshow',val);
        },
        deep:true,
      },
      dialogshow:{
        handler(val){
          this.mydialog=this.dialogshow;
        },
        deep:true,
        immediate:true,
      },
    },
}
</script>

